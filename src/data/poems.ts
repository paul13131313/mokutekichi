/** 住所テキストからマッチするポエムをランダムに1つ返す */
export function getPoem(address: string): string {
  for (const entry of poemData) {
    if (entry.keywords.some(kw => address.includes(kw))) {
      return entry.poems[Math.floor(Math.random() * entry.poems.length)]
    }
  }
  // どこにもマッチしない場合（海外など）
  const fallback = [
    'この地に光の柱が立つ。あなたの目的地が、ここに生まれる。',
    '世界のどこかに、あなたの光景がある。',
    'ここが、あなたの目的地。',
  ]
  return fallback[Math.floor(Math.random() * fallback.length)]
}
